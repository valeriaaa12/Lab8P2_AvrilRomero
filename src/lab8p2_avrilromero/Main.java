    public int contarEstilo(String Estilo, ArrayList<Jugador> x) {
        int cont = 0;
        for (Jugador jugador : x) {
            if (Estilo.equals(jugador.getEstilo())) {
                cont++;
            }
        }
        return cont;
    }

